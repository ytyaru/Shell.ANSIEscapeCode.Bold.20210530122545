#!/usr/bin/env bash
set -Ceu
#---------------------------------------------------------------------------
# ターミナルの画面を装飾する。太字。
# CreatedAt: 2021-05-30
#---------------------------------------------------------------------------
Run() {
	echo -e "\e[1m太字強調\e[m"
	printf '\033[1m%s\033[m\n' '太字強調'
	tput bold && echo "太字強調" && tput sgr0
}
Run "$@"
